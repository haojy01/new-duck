package org.donald.duck.util.search;

public class BinarySearch {

	public static void main(String[] args) {

		System.out.println(14 % 3);
		System.out.println(14 % -3);
		System.out.println(-14 % 3);

		System.out.println(3 & 2);
		System.out.println((0 + 15) / 2);

		System.out.println(1.1 / 2);
	}
}
